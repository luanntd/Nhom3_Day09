# =============================================================================
# RETRIEVAL — DENSE (Vector Search)
# =============================================================================

def retrieve_dense(query: str, top_k: int = TOP_K_SEARCH) -> List[Dict[str, Any]]:
    """
    Dense retrieval: tìm kiếm theo embedding similarity trong ChromaDB.

    Args:
        query: Câu hỏi của người dùng
        top_k: Số chunk tối đa trả về

    Returns:
        List các dict, mỗi dict là một chunk với:
          - "text": nội dung chunk
          - "metadata": metadata (source, section, effective_date, ...)
          - "score": cosine similarity score
    """
    import chromadb
    from pathlib import Path
    from index import get_embedding, CHROMA_DB_DIR

    try:
        # Khởi tạo ChromaDB client
        client = chromadb.PersistentClient(path=str(CHROMA_DB_DIR))
        collection = client.get_collection("rag_lab")

        # Embed query
        query_embedding = get_embedding(query)

        # Query ChromaDB
        results = collection.query(
            query_embeddings=[query_embedding],
            n_results=top_k,
            include=["documents", "metadatas", "distances"]
        )

        # Convert distance to similarity score
        # ChromaDB cosine distance: 1 - similarity
        # Score = 1 - distance (chiều từ 0 đến 1)
        retrieved_chunks = []
        for doc, metadata, distance in zip(
            results["documents"][0],
            results["metadatas"][0],
            results["distances"][0]
        ):
            score = 1 - distance
            retrieved_chunks.append({
                "text": doc,
                "metadata": metadata,
                "score": score,
            })

        return retrieved_chunks

    except Exception as e:
        print(f"Lỗi trong retrieve_dense: {e}")
        return []


# =============================================================================
# RETRIEVAL — SPARSE / BM25 (Keyword Search)
# Dùng cho Sprint 3 Variant hoặc kết hợp Hybrid
# =============================================================================

def retrieve_sparse(query: str, top_k: int = TOP_K_SEARCH) -> List[Dict[str, Any]]:
    """
    Sparse retrieval: tìm kiếm theo keyword (BM25).

    Mạnh ở: exact term, mã lỗi, tên riêng (ví dụ: "ERR-403", "P1", "refund")
    Hay hụt: câu hỏi paraphrase, đồng nghĩa
    """
    import chromadb
    from rank_bm25 import BM25Okapi
    from index import CHROMA_DB_DIR

    try:
        client = chromadb.PersistentClient(path=str(CHROMA_DB_DIR))
        collection = client.get_collection("rag_lab")
        results = collection.get(include=["documents", "metadatas"])

        corpus = [doc for doc in results["documents"]]
        metadatas = results["metadatas"]
        tokenized_corpus = [doc.lower().split() for doc in corpus]
        bm25 = BM25Okapi(tokenized_corpus)

        tokenized_query = query.lower().split()
        scores = bm25.get_scores(tokenized_query)
        top_indices = sorted(range(len(scores)), key=lambda i: scores[i], reverse=True)[:top_k]

        retrieved_chunks = []
        for idx in top_indices:
            retrieved_chunks.append({
                "text": corpus[idx],
                "metadata": metadatas[idx],
                "score": float(scores[idx]),
            })

        return retrieved_chunks

    except Exception as e:
        print(f"Lỗi trong retrieve_sparse: {e}")
        return []


# =============================================================================
# RETRIEVAL — HYBRID (Dense + Sparse với Reciprocal Rank Fusion)
# =============================================================================

def retrieve_hybrid(
    query: str,
    top_k: int = TOP_K_SEARCH,
    dense_weight: float = 0.6,
    sparse_weight: float = 0.4,
) -> List[Dict[str, Any]]:
    """
    Hybrid retrieval: kết hợp dense và sparse bằng Reciprocal Rank Fusion (RRF).
    """
    dense_results = retrieve_dense(query, top_k=top_k)
    sparse_results = retrieve_sparse(query, top_k=top_k)

    combined: Dict[str, Dict[str, Any]] = {}

    for rank, chunk in enumerate(dense_results, start=1):
        key = chunk["text"]
        combined[key] = {
            "chunk": chunk,
            "dense_rank": rank,
            "sparse_rank": None,
        }

    for rank, chunk in enumerate(sparse_results, start=1):
        key = chunk["text"]
        if key in combined:
            combined[key]["sparse_rank"] = rank
        else:
            combined[key] = {
                "chunk": chunk,
                "dense_rank": None,
                "sparse_rank": rank,
            }

    def rrf_score(rank_value: Optional[int]) -> float:
        return 1.0 / (60 + rank_value) if rank_value is not None else 0.0

    merged_chunks = []
    for entry in combined.values():
        score = (
            dense_weight * rrf_score(entry["dense_rank"]) +
            sparse_weight * rrf_score(entry["sparse_rank"])
        )
        chunk = entry["chunk"].copy()
        chunk["score"] = score
        merged_chunks.append(chunk)

    merged_chunks.sort(key=lambda c: c["score"], reverse=True)
    return merged_chunks[:top_k]


